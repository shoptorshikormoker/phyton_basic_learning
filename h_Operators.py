 #Arithmetic Operators
a = 10
b = 3
print("Arithmetic Operators:")
print(f"a + b = {a + b}")
print(f"a - b = {a - b}")
# Comparison Operators
print(f"a == b: {a == b}") 
print(f"a != b: {a != b}")
# Assignment Operators
c = 5
print(f"c = {c}")       
c += 2
print(f"c += 2: {c}")
# Bitwise Operators
m = 5  # Binary: 0101
n = 3  # Binary: 0011
print(f"m & n: {m & n}")  # Bitwise AND
print(f"m | n: {m | n}")