# Creating strings
string1 = 'Hello'
string2 = "World"

string3 = '''This is
a multi-line string'''

# Accessing characters
print(string1[0])  # output: 'H' (first character)
print(string2[-1]) # output: 'd' (last character)

# Slicing
print(string1[1:4])  # 'ell'

# Concatenation
print(string1 + ' ' + string2)  # 'Hello World'

# Repetition
print(string1 * 3)  # 'HelloHelloHello'

# Length
print(len(string1))  # 5
